package mic.crm.common.customer;

public class CustomerDetailsENT {
	String scopeEn;
	String scopeFa;
	String addressEn;
	String addressFa;
	Integer employeeNo;
	Integer id;
	String clientNo;
	
	public String getScopeEn() {
		return scopeEn;
	}
	public void setScopeEn(String scopeEn) {
		this.scopeEn = scopeEn;
	}
	public String getScopeFa() {
		return scopeFa;
	}
	public void setScopeFa(String scopeFa) {
		this.scopeFa = scopeFa;
	}
	public String getAddressEn() {
		return addressEn;
	}
	public void setAddressEn(String addressEn) {
		this.addressEn = addressEn;
	}
	public String getAddressFa() {
		return addressFa;
	}
	public void setAddressFa(String addressFa) {
		this.addressFa = addressFa;
	}
	public Integer getEmployeeNo() {
		return employeeNo;
	}
	public void setEmployeeNo(Integer employeeNo) {
		this.employeeNo = employeeNo;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getClientNo() {
		return clientNo;
	}
	public void setClientNo(String clientNo) {
		this.clientNo = clientNo;
	}
	
	
}
